#include <stdio.h>
 
int main() {
int N,i,e=0,odd=0,p=0,neg=0;
for(i=0;i<5 ; i++){
    scanf("%d",&N);
    if(N%2 == 0){
        e++;
    }
    else{
        odd++;
    }
    if(N>0){
        p++;
    }
    if(N<0) {
        neg++;
    }
}
printf("%d valor(es) par(es)\n",e);
printf("%d valor(es) impar(es)\n",odd);
printf("%d valor(es) positivo(s)\n",p);
printf("%d valor(es) negativo(s)\n",neg);
 
    return 0;
}
