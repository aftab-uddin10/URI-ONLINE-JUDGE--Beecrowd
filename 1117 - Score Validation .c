#include <stdio.h>
 
int main() {
 double X,c=0,sum=0,avg;
 while(1){
      if(c==2.00){
         break;
     }
     scanf("%lf",&X);
     if(X>=0 && X<=10){
         sum=sum+X;
         c++;
     }
     else{
         printf("nota invalida\n");
     }
 }
 avg= sum/2.00;
 printf("media = %.2lf\n",avg);
 
    return 0;
}
